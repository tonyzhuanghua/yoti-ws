public class Hoover {

    private int hooverX;
    private int hooverY;

    public Hoover(int hooverX, int hooverY) {
        this.hooverX = hooverX;
        this.hooverY = hooverY;
    }

    public int getHooverX() {
        return hooverX;
    }

    public void setHooverX(int hooverX) {
        this.hooverX = hooverX;
    }

    public int getHooverY() {
        return hooverY;
    }

    public void setHooverY(int hooverY) {
        this.hooverY = hooverY;
    }
}
